def greet(name = "Mr. UNKNOWN" , language = "English"):
    if language == "English":
        print(f'hello There,{name}')
    elif language == "Hindi":
        print(f'Namaste,{name}')
    elif language == "spanish":
        print(f'Hola,{name}')
    elif language == "German":
        print(f'Hallo!,{name}')
    elif language == "Italian":
        print(f'Ciao,{name}')